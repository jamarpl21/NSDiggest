@dataclass
class DigestTopic:
    title: str
    summary: str
    links: list[dict]
    duplicate_of: int | None = None


@dataclass
class DigestNewsletter:
    sender: str
    subject: str
    topics: list[DigestTopic]
    original_index: int = -1
    estimated_cost_usd: float = 0.0
    stage1_input_tokens: int = 0
    stage1_output_tokens: int = 0
    processed_with: str = "llm"


@dataclass
class Digest:
    date: str
    newsletters: list[DigestNewsletter]
    processing_mode: ProcessingMode = "llm-only"

    @property
    def topic_count(self) -> int:
        return sum(len(n.topics) for n in self.newsletters)

    @property
    def duplicate_count(self) -> int:
        return sum(1 for n in self.newsletters for t in n.topics if t.duplicate_of is not None)

    @property
    def estimated_cost_usd(self) -> float:
        return sum(n.estimated_cost_usd for n in self.newsletters)


def _estimate_cost_usd(
    input_tokens: int,
    output_tokens: int,
    in_per_mtok: float,
    out_per_mtok: float,
    cache_write_tokens: int = 0,
    cache_read_tokens: int = 0,
    cache_write_per_mtok: float = 0.0,
    cache_read_per_mtok: float = 0.0,
) -> float:
    return (
        (input_tokens / 1_000_000.0) * in_per_mtok
        + (output_tokens / 1_000_000.0) * out_per_mtok
        + (cache_write_tokens / 1_000_000.0) * cache_write_per_mtok
        + (cache_read_tokens / 1_000_000.0) * cache_read_per_mtok
    )


def _repair_topics_with_llm(
    client: Anthropic,
    cfg: Config,
    raw_topics: str,
    idx: int,
    sender: str,
) -> tuple[list[dict], int, int, float]:
    """LLM fallback for malformed topics JSON strings."""
    log.warning("Stage1 idx=%d sender=%r: attempting LLM repair for malformed topics JSON", idx, sender)
    try:
        response = client.messages.create(
            model=cfg.anthropic_model_stage2,
            max_tokens=2048,
            system=[{"type": "text", "text": TOPICS_REPAIR_SYSTEM_PROMPT}],
            tools=[TOPICS_REPAIR_TOOL_SCHEMA],
            tool_choice={"type": "tool", "name": "emit_repaired_topics"},
            messages=[{"role": "user", "content": raw_topics}],
            timeout=120,
        )
    except Exception as exc:
        log.warning(
            "Stage1 idx=%d sender=%r: repair call failed (%s) — continuing without repair",
            idx,
            sender,
            type(exc).__name__,
        )
        return [], 0, 0, 0.0
    usage_in = int(getattr(response.usage, "input_tokens", 0) or 0)
    usage_out = int(getattr(response.usage, "output_tokens", 0) or 0)
    usage_cache_write = int(getattr(response.usage, "cache_creation_input_tokens", 0) or 0)
    usage_cache_read = int(getattr(response.usage, "cache_read_input_tokens", 0) or 0)
    repair_cost = _estimate_cost_usd(
        usage_in,
        usage_out,
        cfg.stage2_input_usd_per_mtok,
        cfg.stage2_output_usd_per_mtok,
        cache_write_tokens=usage_cache_write,
        cache_read_tokens=usage_cache_read,
        cache_write_per_mtok=cfg.stage2_cache_write_usd_per_mtok,
        cache_read_per_mtok=cfg.stage2_cache_read_usd_per_mtok,
    )
    log.info(
        "Stage1 repair response: idx=%d sender=%r usage_in=%d usage_out=%d est_cost_usd=%.6f",
        idx,
        sender,
        usage_in,
        usage_out,
        repair_cost,
    )
    tool_use = next((b for b in response.content if getattr(b, "type", None) == "tool_use"), None)
    if tool_use is None:
        log.warning("Stage1 idx=%d sender=%r: repair call returned no tool_use", idx, sender)
        return [], usage_in, usage_out, repair_cost
    topics = (tool_use.input or {}).get("topics", []) or []
    if isinstance(topics, str):
        try:
            topics = json.loads(topics)
        except json.JSONDecodeError:
            log.warning("Stage1 idx=%d sender=%r: repair call returned non-parseable string topics", idx, sender)
            topics = []
    if isinstance(topics, list):
        return topics, usage_in, usage_out, repair_cost
    log.warning("Stage1 idx=%d sender=%r: repair call returned non-list topics type=%s", idx, sender, type(topics).__name__)
    return [], usage_in, usage_out, repair_cost


def _build_stage1_message(email: FetchedEmail) -> str:
    parts = [
        f"Nadawca: {email.sender_name} <{email.sender_email}>",
        f"Temat: {email.subject}",
        f"Otrzymano: {email.received_at.isoformat()}",
        "",
        "--- text_content (PEŁNA TREŚĆ — przeczytaj do końca) ---",
        email.text_content,
        "",
        "--- raw_links (backup linków z HTML) ---",
    ]
    if email.raw_links:
        for rl in email.raw_links:
            parts.append(f"- {rl.get('text', '')[:80]} → {rl.get('url', '')}")
    else:
        parts.append("(brak)")
    parts.append("")
    parts.append("Wywołaj teraz narzędzie `emit_newsletter_topics` z wyekstrahowanymi tematami.")
    return "\n".join(parts)


def _extract_single_newsletter(
    client: Anthropic,
    cfg: Config,
    idx: int,
    email: FetchedEmail,
    debug_dir: Path | None,
    retry_attempt: bool = False,
    accumulated_input_tokens: int = 0,
    accumulated_output_tokens: int = 0,
    accumulated_cost_usd: float = 0.0,
) -> DigestNewsletter:
    """Stage 1: extract topics from a single newsletter via LLM."""
    user_content = _build_stage1_message(email)
    log.info(
        "Stage1 call: model=%s idx=%d sender=%r input_chars=%d",
        cfg.anthropic_model_stage1, idx, email.sender_name, len(user_content),
    )

    t0 = time.monotonic()
    # Non-streaming: each newsletter takes <5 min so no 10-min timeout issue.
    # Using non-streaming avoids a streaming SDK quirk where large tool_use inputs
    # arrive as a raw JSON string instead of a parsed dict.
    system_prompt = STAGE1_RETRY_SYSTEM_PROMPT if retry_attempt else STAGE1_SYSTEM_PROMPT
    response = client.messages.create(
        model=cfg.anthropic_model_stage1,
        max_tokens=STAGE1_MAX_OUTPUT_TOKENS,
        system=[{
            "type": "text",
            "text": system_prompt,
            "cache_control": {"type": "ephemeral"},
        }],
        tools=[STAGE1_TOOL_SCHEMA],
        tool_choice={"type": "tool", "name": "emit_newsletter_topics"},
        messages=[{"role": "user", "content": user_content}],
        timeout=300,  # 5-minute hard timeout per newsletter
    )
    elapsed = time.monotonic() - t0

    log.info(
        "Stage1 response: idx=%d sender=%r elapsed=%.1fs stop_reason=%s "
        "usage_in=%s usage_out=%s cache_read=%s cache_write=%s",
        idx, email.sender_name, elapsed, response.stop_reason,
        response.usage.input_tokens, response.usage.output_tokens,
        getattr(response.usage, "cache_read_input_tokens", None),
        getattr(response.usage, "cache_creation_input_tokens", None),
    )
    call_input_tokens = int(getattr(response.usage, "input_tokens", 0) or 0)
    call_output_tokens = int(getattr(response.usage, "output_tokens", 0) or 0)
    call_cache_write_tokens = int(getattr(response.usage, "cache_creation_input_tokens", 0) or 0)
    call_cache_read_tokens = int(getattr(response.usage, "cache_read_input_tokens", 0) or 0)
    call_cost_usd = _estimate_cost_usd(
        call_input_tokens,
        call_output_tokens,
        cfg.stage1_input_usd_per_mtok,
        cfg.stage1_output_usd_per_mtok,
        cache_write_tokens=call_cache_write_tokens,
        cache_read_tokens=call_cache_read_tokens,
        cache_write_per_mtok=cfg.stage1_cache_write_usd_per_mtok,
        cache_read_per_mtok=cfg.stage1_cache_read_usd_per_mtok,
    )
    total_input_tokens = accumulated_input_tokens + call_input_tokens
    total_output_tokens = accumulated_output_tokens + call_output_tokens
    total_cost_usd = accumulated_cost_usd + call_cost_usd
    if response.stop_reason == "max_tokens":
        log.warning(
            "Stage1 idx=%d sender=%r hit max_tokens (%d) — output was truncated, topics may be empty. "
            "Consider increasing STAGE1_MAX_OUTPUT_TOKENS or switching to a model with higher output limit.",
            idx, email.sender_name, STAGE1_MAX_OUTPUT_TOKENS,
        )

    tool_use = next((b for b in response.content if getattr(b, "type", None) == "tool_use"), None)

    if debug_dir is not None:
        debug_dir.mkdir(parents=True, exist_ok=True)
        suffix = "-retry" if retry_attempt else ""
        (debug_dir / f"stage1-idx{idx}{suffix}.json").write_text(
            json.dumps(
                {"idx": idx, "sender": email.sender_name, "tool_input": tool_use.input if tool_use else None},
                ensure_ascii=False, indent=2,
            ),
            encoding="utf-8",
        )

    if tool_use is None:
        if not retry_attempt:
            log.warning(
                "Stage1 idx=%d sender=%r: no tool_use on first attempt — retrying once",
                idx,
                email.sender_name,
            )
            return _extract_single_newsletter(
                client=client,
                cfg=cfg,
                idx=idx,
                email=email,
                debug_dir=debug_dir,
                retry_attempt=True,
                accumulated_input_tokens=total_input_tokens,
                accumulated_output_tokens=total_output_tokens,
                accumulated_cost_usd=total_cost_usd,
            )
        log.warning("Stage1 idx=%d sender=%r: no tool_use after retry — inserting empty newsletter", idx, email.sender_name)
        return DigestNewsletter(
            sender=email.sender_name or email.sender_email,
            subject=email.subject,
            topics=[],
            original_index=idx,
            estimated_cost_usd=total_cost_usd,
            stage1_input_tokens=total_input_tokens,
            stage1_output_tokens=total_output_tokens,
        )

    data = tool_use.input or {}
    raw_topics = data.get("topics", []) or []
    # Guard: model sometimes returns topics as JSON-encoded string instead of parsed list.
    # Summaries with literal newlines inside strings make the JSON technically invalid, so
    # we try strict mode first, then lenient mode (strict=False allows control chars in strings).
    if isinstance(raw_topics, str):
        parsed_topics = None
        try:
            parsed_topics = json.loads(raw_topics)
        except json.JSONDecodeError as exc:
            sanitized = _sanitize_json_control_chars(raw_topics)
            try:
                parsed_topics = json.loads(sanitized)
            except json.JSONDecodeError:
                log.warning(
                    "Stage1 idx=%d sender=%r: topics JSON parse failed at pos=%d (%s). first_120=%r",
                    idx, email.sender_name, exc.pos, exc.msg, raw_topics[:120],
                )
        if isinstance(parsed_topics, list):
            raw_topics = parsed_topics
            log.debug(
                "Stage1 idx=%d sender=%r: topics was JSON string — parsed %d items",
                idx, email.sender_name, len(raw_topics),
            )
        else:
            repaired, repair_in, repair_out, repair_cost = _repair_topics_with_llm(client, cfg, raw_topics, idx, email.sender_name)
            total_input_tokens += repair_in
            total_output_tokens += repair_out
            total_cost_usd += repair_cost
            if repaired:
                raw_topics = repaired
                log.info(
                    "Stage1 idx=%d sender=%r: topics repaired via LLM fallback (%d items)",
                    idx, email.sender_name, len(raw_topics),
                )
            else:
                raw_topics = []

    topics: list[DigestTopic] = []
    for t in raw_topics:
        if not isinstance(t, dict):
            continue
        title = (t.get("title") or "").strip()
        summary = (t.get("summary") or "").strip()
        if not title or not summary:
            continue
        links = [lk for lk in (t.get("links") or []) if isinstance(lk, dict) and lk.get("url")]
        topics.append(DigestTopic(title=title, summary=summary, links=links))

    sender = (data.get("sender") or "").strip() or email.sender_name or email.sender_email
    subject = (data.get("subject") or "").strip() or email.subject
    if not topics and not retry_attempt:
        log.warning(
            "Stage1 idx=%d sender=%r: zero parsed topics on first attempt — retrying once",
            idx,
            email.sender_name,
        )
        return _extract_single_newsletter(
            client=client,
            cfg=cfg,
            idx=idx,
            email=email,
            debug_dir=debug_dir,
            retry_attempt=True,
            accumulated_input_tokens=total_input_tokens,
            accumulated_output_tokens=total_output_tokens,
            accumulated_cost_usd=total_cost_usd,
        )
    return DigestNewsletter(
        sender=sender,
        subject=subject,
        topics=topics,
        original_index=idx,
        estimated_cost_usd=total_cost_usd,
        stage1_input_tokens=total_input_tokens,
        stage1_output_tokens=total_output_tokens,
    )


def _build_stage2_message(newsletters: list[DigestNewsletter]) -> str:
    payload = []
    for nl in newsletters:
        payload.append({
            "newsletter_idx": nl.original_index,
            "sender": nl.sender,
            "topics": [
                {"topic_idx": i, "title": t.title, "summary": t.summary}
                for i, t in enumerate(nl.topics)
            ],
        })
    return (
        "Poniżej kompaktowe podsumowanie wszystkich newsletterów dnia. "
        "Zidentyfikuj tematy, które dotyczą dokładnie tego samego zdarzenia/artykułu.\n\n"
        + json.dumps(payload, ensure_ascii=False, indent=2)
        + "\n\nWywołaj teraz narzędzie `emit_dedup_assignments`."
    )


def _sender_key(email: FetchedEmail) -> str:
    sender_email = (email.sender_email or "").strip().lower()
    sender_name = (email.sender_name or "").strip().lower()
    return sender_email or sender_name or "unknown-sender"


