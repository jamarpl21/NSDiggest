def _sanitize_json_control_chars(raw: str) -> str:
    """Best-effort repair of malformed JSON generated by the model."""
    out: list[str] = []
    in_string = False
    escaped = False
    i = 0
    n = len(raw)

    while i < n:
        ch = raw[i]
        if in_string:
            if escaped:
                out.append(ch)
                escaped = False
                i += 1
                continue
            if ch == "\\":
                out.append(ch)
                escaped = True
                i += 1
                continue
            if ch == "\"":
                # If quote is not followed by a valid delimiter, treat it as
                # an unescaped quote inside the string and escape it.
                j = i + 1
                while j < n and raw[j] in (" ", "\t", "\r", "\n"):
                    j += 1
                next_token = raw[j] if j < n else ""
                if next_token not in (",", "}", "]", ":"):
                    out.append("\\\"")
                    i += 1
                    continue
                out.append(ch)
                in_string = False
                i += 1
                continue
            if ch == "\n":
                out.append("\\n")
                i += 1
                continue
            if ch == "\r":
                out.append("\\r")
                i += 1
                continue
            if ch == "\t":
                out.append("\\t")
                i += 1
                continue
            out.append(ch)
            i += 1
            continue

        out.append(ch)
        if ch == "\"":
            in_string = True
        i += 1

    return "".join(out)


